public class EqualsOrNot {
    public static void main(String[] args) {
        System.out.println("asdasd is not equals to asdasd");
        System.out.println("asd‏asd".equals("asdasd"));

        System.out.println("asdasd is still not equals to asdasd");
        System.out.println("asd‏asd" == "asdasd");

        System.out.println("asdasd is now equals to asdasd");
        System.out.println("asdasd" == "asdasd");
    }
}
